package com.math.member;

public class MemberDTO {

	private String id;
	private String pw;
	private String name;
	private String address;
	private int goal;
	private String birth;
	private String level_avg;
	private String phone;
	private String join_date;
	private String use_date;
	private String auto_payment;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getGoal() {
		return goal;
	}
	public void setGoal(int goal) {
		this.goal = goal;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getLevel_avg() {
		return level_avg;
	}
	public void setLevel_avg(String level_avg) {
		this.level_avg = level_avg;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getJoin_date() {
		return join_date;
	}
	public void setJoin_date(String join_date) {
		this.join_date = join_date;
	}
	public String getUse_date() {
		return use_date;
	}
	public void setUse_date(String use_date) {
		this.use_date = use_date;
	}
	public String getAuto_payment() {
		return auto_payment;
	}
	public void setAuto_payment(String auto_payment) {
		this.auto_payment = auto_payment;
	}

}
