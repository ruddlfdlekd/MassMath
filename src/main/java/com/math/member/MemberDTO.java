package com.math.member;

public class MemberDTO {
	
	private String id;
	private String pw;
	private String name;
	private String address;
	private String birth;
	private String goal;
	private String level_age;
	private String phone;
	private String join_date;
	private String user_date;
	private int auto_payment;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getGoal() {
		return goal;
	}
	public void setGoal(String goal) {
		this.goal = goal;
	}
	public String getLevel_age() {
		return level_age;
	}
	public void setLevel_age(String level_age) {
		this.level_age = level_age;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getJoin_date() {
		return join_date;
	}
	public void setJoin_date(String join_date) {
		this.join_date = join_date;
	}
	public String getUser_date() {
		return user_date;
	}
	public void setUser_date(String user_date) {
		this.user_date = user_date;
	}
	public int getAuto_payment() {
		return auto_payment;
	}
	public void setAuto_payment(int auto_payment) {
		this.auto_payment = auto_payment;
	}
	
}
